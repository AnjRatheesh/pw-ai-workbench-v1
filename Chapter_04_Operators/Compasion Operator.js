/*comparison operators are used to compare two values and return a boolean value(true or false) based on the comparison.Here are some common comparison operators in JavaScript:

1. == (Equality): Checks if the values of two operands are equal, ignoring their data types.
    Example:
console.log(5 == '5'); // true   
2. === (Strict Equality): Checks if the values and data types of two operands are equal.
    Example:
console.log(5 === '5'); // false 
3. != (Inequality): Checks if the values of two operands are not equal, ignoring their data types.
    Example:
console.log(5 != '5'); // false 
4. !== (Strict Inequality): Checks if the values and data types of two operands are not equal.
    Example:
console.log('5 !== '5); // true
5. > (Greater Than): Checks if the value of the left operand is greater than the value of the right operand.
    Example:
console.log(10 > 5); // true
6. < (Less Than): Checks if the value of the left operand is less than the value of the right operand.
    Example:
console.log(3 < 7); // true
7. >= (Greater Than or Equal To): Checks if the value of the left operand is greater than or equal to the value of the right operand.
    Example:
console.log(5 >= 5);
8. <= (Less Than or Equal To): Checks if the value of the left operand is less than or equal to the value of the right operand.
    Example:
console.log(4 <= 6); // true    
console.log(5 <= 5); // true            

*/