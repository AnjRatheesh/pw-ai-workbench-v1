/*
String Opeartor   is used to perform operations on strings. Here are some common string operators in JavaScript:

1. + (Concatenation): Combines two or more strings into one string.     
Example:
let str1 = "Hello, ";
let str2 = "world!";
let result = str1 + str2; // result is "Hello, world!"

2. += (Concatenation Assignment): Appends the right operand to the left operand and assigns the result to the left operand.
Example:
let str = "Hello, ";
str += "world!"; // str is now "Hello, world!"

3. [] (Indexing): Accesses a specific character in a string based on its index (starting from 0).
Example:
let str = "Hello";
console.log(str[0]);
// Output: "H"
console.log(str[1]);        
// Output: "e"  

4. length (String Length): Returns the number of characters in a string.
Example:
let str = "Hello";
console.log(str.length); // Output: 5  


5. template literals (String Interpolation): Allows embedding expressions within a string using backticks (`) and ${} syntax.
Example:
let name = "Alice";
let greeting = `Hello, ${name}!`; // greeting is "Hello, Alice!"
*/